package itc.hoseo.soolfarm.delivery;

public class DeliveryVO {
	
	
	private int gdNum;
	private String gdName;
	private String gdKinds;
	private String gdPrice;
	private String gdMu;
	private String gdDiscript;
	private String gdStatus;
	
	
	public int getGdNum() {
		return gdNum;
	}
	public void setGdNum(int gdNum) {
		this.gdNum = gdNum;
	}
	public String getGdName() {
		return gdName;
	}
	public void setGdName(String gdName) {
		this.gdName = gdName;
	}
	public String getGdKinds() {
		return gdKinds;
	}
	public void setGdKinds(String gdKinds) {
		this.gdKinds = gdKinds;
	}
	public String getGdPrice() {
		return gdPrice;
	}
	public void setGdPrice(String gdPrice) {
		this.gdPrice = gdPrice;
	}
	public String getGdMu() {
		return gdMu;
	}
	public void setGdMu(String gdMu) {
		this.gdMu = gdMu;
	}
	public String getGdDiscript() {
		return gdDiscript;
	}
	public void setGdDiscript(String gdDiscript) {
		this.gdDiscript = gdDiscript;
	}
	public String getGdStatus() {
		return gdStatus;
	}
	public void setGdStatus(String gdStatus) {
		this.gdStatus = gdStatus;
	}
	

}
